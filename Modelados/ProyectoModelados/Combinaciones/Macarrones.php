                        <h4 class="card-text">Pasos a seguir:</h4>
                        <p class="card-text">Ponemos a calentar un olla con agua salada, para cocer en ella la pasta, que dejaremos un punto mas dura que al dente para terminarlos de cocinar en el horno, que tambien iremos precalentantando a 200C.
Mientras se cuece la pasta, prepararamos la salsa de los macarrones, una especie de bechamel elaborada con leche y la harina de maiz. Para ello, en una sarten, mezclamos esa harina diluida en un poco de leche fria y luego a�adimos el resto de la leche, la cucharadita de mostaza, una pizca de sal y otra de pimienta. Mezclamos bien y calentamos a fuego medio.
Es muy importante no dejar de remover mientras calentamos, hasta que la salsa ligue. Cuando se haya calentado un poco la leche echamos la mantequilla, sin dejar de remover bien, hasta que la salsa se convierta en una crema espesa. Luego bajamos el fuego al minimo y a�adimos el queso cheddar, removiendo de nuevo hasta que se incorpore.
Cuando la pasta este cocida, la mezclamos con la salsa y la colocamos en una fuente de horno. Cubrimos con el queso parmesano rallado y horneamos en la parte media-alta durante 15 minutos, gratinando despues otros tres minutos mas, hasta que se haya dorado bien por arriba.</p>
